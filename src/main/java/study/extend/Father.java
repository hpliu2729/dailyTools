package study.extend;

public class Father {
	
	private String name;
	private String sex;
	public Father(){
		this.name = "father";
		this.sex = "man";
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	

}
